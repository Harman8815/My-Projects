    public void converterFC(){
        float fer=Float.parseFloat(fString);
        float ans=(fer-32)*5/9;
        System.out.println(ans);
        cString=Float.toString(ans);
        cString+=" °C";
        fString+=" °F";
    }
    public void converterCF(){
        float celsius=Float.parseFloat(cString);
        float ans=( (celsius*9)/5)+32; 
        fString=Float.toString(ans);
        fString+=" °F";
        cString+=" °C";
        
    }
